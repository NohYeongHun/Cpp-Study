#include <iostream>
using namespace std;

// 오늘의 주제 : 템플릿 기초

class Player
{
public:

public:
	int _hp = 100;
};

template<typename T>
void Print(T a)
{
	cout << a << endl;
}

template<typename T1, typename T2>
void Print(T1 a, T2 b)
{
	cout << a << " " << b << endl;
}


template<typename T>
T Add(T a, T b)
{
	return a + b;
}

// 연산자 오버로딩 (전역함수 버전)
ostream& operator<<(ostream& os, const Player& p)
{
	os << p._hp;
	return os;
}

// 템플릿 특수화
template<>
void Print(Player a)
{
	cout << "Player !!!!!!!!!!!!!" << endl;
	cout << a._hp << endl;
}

int main()
{
	// 템플릿 : 함수나 클래스를 찍어내는 틀
	Print<int>(50);
	Print(50.01f);
	Print(50.03);
	Print<const char*>("Hello World");

	int result1 = Add(1, 2);
	float result2 = Add<float>(1.f, 2.5f);

	Player p;
	Print(p);
	

	return 0;
}